#include "drivers\nrf5x_system.h"

#include "imuCube.h"
/*====================================================================================================*/
/*====================================================================================================*/
void IMUCube_Init( void )
{
  IMUCube_CKOCK_Config();
  IMUCube_GPIO_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void IMUCube_Loop( void )
{
  while(1) {
    while(KEY_Read()) {
      BUZ_Toggle();
      MOT_Set();
      delay_ms(1);
    }
    MOT_Reset();
    BUZ_Reset();
  }
}
